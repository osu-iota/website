        <h4 id="2.3">Section 2.3: On-campus Capstones</h4>
        <p>Each On-campus Capstone will organize student teams into domains that reflect the nature of each team's
            project. Each student Capstone team will be in one of these domains. One or more of these domains in CS, and
            one or more of these domains in ECE, will be related to IoT (though its name might be something different).
            The domains in CS Capstone will likely differ from the domains in ECE Capstone. The point is that student
            teams will be grouped so that all the teams within each Capstone now have a domain.</p>
        <p>Each Capstone will then assign each domain to a TA, recognizing that some TAs might be assigned to multiple
            domains if some domains have relatively few teams. Each Capstone TA will have the option to organize events
            that students within that domain may attend so that teams can learn from one another and/or collaborate on
            components of mutual interest; however, this activity will not be required as part of the TAs expected
            duties for FTEs. For example, one of the domains in CS Capstone might be related to embedded devices, and
            students in that domain's teams might decide host an event to create certain components that all of them can
            then reuse in their respective Capstone projects.</p>
        <p>The CS and ECE Capstone TAs who oversee domains related to IoT have the option of notifying each other of
            events that they coordinate, so that these TAs can advertise one another's events, if they so choose. In
            addition, they might notify the IOTA Community Coordinator of these events, so that other members of IOTA
            may attend the events. Furthermore, to the extent possible, the Community Coordinator can collect resources
            on event topics that could be placed in the IOTA online repository (Section 2.1).</p>
        <p>Finally, both On-Campus Capstone representatives affirm the desirability of letting appropriately-qualified
            ECE seniors register for CS Capstone instead of ECE Capstone, and vice versa, and they express a commitment
            to work toward necessary policy and procedural changes needed to make that possible.</p>

        <h5 id="2.3.1">Section 2.3.1: Specific ECE Capstone Commitments</h5>
        <p>Don Heer (<a href="mailto:heer@eecs.orst.edu">heer@eecs.orst.edu</a>) represents the ECE Capstone.</p>
        <p>In addition to the commitments above, the ECE Capstone commits to allocating one of its 0.25 GTAs, assuming
            the course has at least 1.0 TA assignment, to serve as an "at large" resource for the Alliance as a whole,
            if EECS provides at least one TA with professional industrial experience developing of IoT systems. This
            means that this "at large" TA will hold office hours every week that any IOTA members' students may attend,
            and this TA will make his or her contact information and availability known to the IOTA Community
            Coordinator for publication on the IOTA website. In addition, time permitting, this TA will invest time and
            effort in helping to create shareable resources that the IOTA Community coordinator can also publish.</p>
        <p>Furthermore, the ECE Capstone is willing to share meeting space (DB211) for short events or 24-hour use,
            which includes testing tools, to other members of IOTA and/or student groups associated with those members.
            To request permission for using this space, other member representatives must contact the "at large" TA, who
            may impose constraints on when and how the space and tools may be used, and/or may require that visitors
            only use the space when this TA is present.</p>
        <p>The ECE Capstone will grant its Capstone students credit for some assignments associated with professional
            development if students participate in events organized by IOTA members. (Students will also have other
            options for fulfilling this professional development assignment.) These events may include domain-specific
            events organized by the ECE domain-specific TAs, or they may include events organized by other IOTA members
            (such as those held by the ECE Capstone domain-specific TAs, or the Alliance's member clubs). Students'
            credit will be based on participation metrics provided by the IOTA Community Coordinator (Section 2.1),
            which will take into account students' professional development, technical performance, presentations,
            collaboration, and project management.</p>
        <p>The ECE Capstone is open to providing a small amount of funding to other members of IOTA for purchasing
            materials and supplies for events. Member representatives should contact the ECE Capstone representative for
            details.</p>
        <p>ECE capstone will also provide limited faculty time to assist the alliance in forming and adapting to the
            needs of the alliance members.</p>

