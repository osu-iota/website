        <h4 id="2.1">Section 2.1: School of Electrical Engineering and Computer Science (EECS)</h4>
        <p>Tom Weller (<a href="mailto:tom.weller@oregonstate.edu">tom.weller@oregonstate.edu</a>) represents EECS.</p>
        <p>EECS will fund one 0.49 GTA who will serve as the "IOTA Community Coordinator,” who will be responsible for
            the following activities:</p>
        <ul>
            <li>Creating an IOTA website, or a section within the EECS website, that will contain several sections:
                <ul>
                    <li>An up to date copy of this IOTA charter</li>
                    <li>An online calendar reflecting all member events relevant to the Alliance</li>
                    <li>A repository of digital resources shareable by all Alliance members (e.g., training materials,
                        datasets, and reusable software components)
                    </li>
                    <li>A catalog of non-digital resources that members have indicated they are open to sharing with
                        other Alliance members (e.g., tools and hardware)
                    </li>
                    <li>An online portfolio system that promotes the Alliance's reputation by showcasing what its
                        members and their students are accomplishing
                    </li>
                    <li>A record of which students have led seminars and other member events in the past, to facilitate
                        hiring of those students (e.g., post-graduation or as GAs)
                    </li>
                </ul>
            </li>
            <li>Coordinating with members to help them obtain resources needed for member events</li>
            <li>Coordinating with other EECS staff to showcase student projects originating in the IoT Alliance at
                industry-oriented events that EECS generally holds to promote the reputation of students and the school
            </li>
            <li>Helping members to accumulate and improve resources (e.g., updating training materials after seminars)
            </li>
            <li>Creating and managing one or more mailing list to facilitate communications among member representatives
                and/or students associated with members
            </li>
            <li>Maintaining a database of student participation in events (e.g., seminars) by consolidating data
                provided by members after events occur
                <ul>
                    <li>This data will indicate several consolidated metrics for each student.</li>
                    <li>The Community Coordinator will work with the Alliance's member representatives (particularly
                        Capstone leaders) to precisely define appropriate metrics to track.
                    </li>
                    <li>The Community Coordinator will provide these consolidated data to Alliance members in a manner
                        that respects both student privacy and members' instructional interests
                    </li>
                </ul>
            </li>
            <li>Ensuring that all collection and sharing of information related to student activities is done in a
                manner compliant with university policies
            </li>
        </ul>
        <p>The Community Coordinator will interact regularly with all members of the Alliance and will ultimately report
            to the representative of the MSSWI Track.</p>
        <p>Due to the fact that effectively satisfying the job description above will require skills in both web
            development and in engineering of IoT systems, it is possible that no single TA at OSU has all the necessary
            abilities. Therefore, instead of providing a single 0.49 GTA with all of these skills, it is possible that
            EECS will instead sometimes provide two 0.25 GTAs who have complementary skills and can divide the
            responsibilities.</p>
        <p>In addition to the GTA support, above, EECS will allow Alliance members to reserve conference rooms in Kelley
            Engineering Center for conducting IOTA events on weekends and after 5pm on weekdays. Specifically, IOTA
            member representatives (faculty) may request access to the Kelley conference room scheduling system, and
            these representatives may then reserve space on evenings and weekends. IOTA members may be asked by EECS to
            restrict their use of space only to certain rooms. It is understood that IOTA does not enjoy any particular
            priority over other groups for use of this conference room space, and therefore rooms may not be available
            during certain times and dates.</p>

