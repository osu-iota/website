        <h3 id="1">Section 1: General Provisions</h3>
        <h4 id="1.1">Section 1.1: Alliance Membership</h4>
        <p>IOTA is a type of a community of practice: It is a community of members from different backgrounds who help
            one another to master a shared area of technical practice, specifically engineering of IoT systems. These
            members are the following:</p>
        <ul>
            <li>The School of Electrical Engineering and Computer Science (EECS)</li>
            <li>The Computer Science (CS) Online Capstone</li>
            <li>The CS On-campus Capstone</li>
            <li>The CS On-campus Capstone</li>
            <li>The Master's in Software Innovation (MSSWI) Track</li>
            <li>The CS Network Security Course</li>
            <li>The CS Undergraduate Operating Systems Courses</li>
            <li>The OSU Robotics Club</li>
            <li>The Hackathon Club</li>
            <li>The Security Club</li>
            <li>Inventors Enterprise Club</li>
        </ul>
        <p>Each member is responsible on a per-academic year basis for identifying one individual to represent that
            member's interests in discussions concerning the Alliance. The member will arrange for that individual's
            name and contact information to appear in Section 2 of this Charter.</p>
        <p>Additional parties may become members of this Alliance with the unanimous consent of existing members. A
            member may be removed from the Alliance by unanimous consent of the other members. An existing member may
            initiate a motion to admit a new member, or to remove an existing member, by sending an email to all
            representatives of existing members. If no member publicly objects via email to the admission or removal
            within one month of this email, then the membership list in Section 1 of this Charter will be changed as
            proposed.</p>
        <p>EECS will publish and maintain an up-to-date copy of this IOTA Charter in a prominent location online,
            reflecting modifications that may occur.</p>
        <p>The current members affirm their intention to maintain a welcoming culture of participation. Therefore, they
            express a desire generally to admit any new members who offer significant new contributions aligned with the
            interests of the Alliance. Likewise, members affirm an intention not to expel one another from the Alliance,
            except in cases where a member routinely fails to keep commitments to the Alliance.</p>

        <h4 id="1.2">Section 1.2: Charter Modifications</h4>
        <p>The members of this Alliance may, by unanimous consent, modify Section 1 of this Charter. A member may move
            to initiate changes to Section 1 by sending an email to all representatives and, if no objections are raised
            publicly within 1 month of this email, the Charter will be changed as proposed.</p>
        <p>In addition, each member may unilaterally modify the respective subsection of Section 2 or withdraw from
            membership by sending an email to all representatives of members, and the Charter will immediately be
            changed as proposed.</p>
        <p>The members of this Alliance affirm their intent that any modifications to this Charter will be consistent
            with the spirit of the Mission Statement above. Members affirm their intention to provide one another with
            timely warning of future changes that could negatively impact one another, in order that members can
            accommodate changes in others’ commitments.</p>

        <h4 id="1.3">Section 1.3: Alliance Objectives</h4>
        <p>Each member of this Alliance commits to the following core objectives, to the extent that doing so is
            feasible and beneficial for the member:</p>
        <ul>
            <li>To conduct events, such as non-credit seminars, that are of mutual interest to other members of the
                Alliance
            </li>
            <li>To share resources such as documents, training materials, data, software components, tools and other
                hardware
            </li>
            <li>To contribute new resources of potential value to the Alliance and its members</li>
            <li>To help IOTA as a whole, as well as individual students, to earn a reputation for engineering of IoT
                systems
            </li>
            <li>To promote an inclusive, diverse community that respects the distinct interests of other members and
                their students
            </li>
        </ul>
        <p>Each member recognizes that other members pursue these core objectives in varied ways, due to differences in
            the members' priorities, interests and constraints. The members further recognize that each other member's
            interests extend beyond engineering of IoT systems, so other members may at times need to make choices that
            are not perfectly aligned with the interests of the Alliance. Nonetheless, the members of IOTA commit to
            invest time and effort to collaboratively support student success as much as possible in the area of
            practicing engineering of IoT systems.</p>

