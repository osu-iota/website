        <h1>Internet-of-Things Alliance (IOTA) Charter</h1>
        <h3>Mission Statement</h3>
        <p>The parties to this Charter affirm their desire to prepare students at Oregon State University for careers in
            practicing engineering of Internet-of-Things (IoT) systems. Such technologies, exemplified by
            building-monitoring systems as well as networks of devices and/or bots, have grown in economic and cultural
            importance. Therefore, the parties to this Charter commit to form a community wherein students develop
            relevant practice-oriented skills, identify personally meaningful problems, take initiative, design
            technical solutions, obtain support, implement solutions, give and receive mentorship, interact with
            industry and university experts, serve as leaders in professionalism and innovation, and showcase IoT
            systems.</p>

